package test.java.argo.cost.menu;

import static org.junit.Assert.assertEquals;

import org.junit.Test;

import argo.cost.menu.model.UserInfo;
import argo.cost.menu.model.UserKengen;
import argo.cost.menu.service.LoginServiceImpl;

public class MenuTest {
	
	
	@Test
	public void testGetUserInfo() {
		
		LoginServiceImpl loginServiceImpl = new LoginServiceImpl();
		UserInfo user = new UserInfo();
		UserInfo userInfo = new UserInfo();
		
		user.setUserId("xiongyl");
		
		if (user.getUserId().equals("xiongyl")) {
			
			userInfo = loginServiceImpl.getUserInfo(user);
			assertEquals("熊燕玲", userInfo.getName());
			assertEquals("xiongyl", userInfo.getPassword());
			
		}
		
        user.setUserId("caowy");
		
		if (user.getUserId().equals("caowy")) {
			
			userInfo = loginServiceImpl.getUserInfo(user);
			assertEquals("曹文艶", userInfo.getName());
			assertEquals("caowy", userInfo.getPassword());
			
		}
		
        user.setUserId("liuyj");
		
		if (user.getUserId().equals("liuyj")) {
			
			userInfo = loginServiceImpl.getUserInfo(user);
			assertEquals("劉亜傑", userInfo.getName());
			assertEquals("liuyj", userInfo.getPassword());
		}
		
	}

	@Test
	public void testGetUserKengen() {
		
		LoginServiceImpl loginServiceImpl = new LoginServiceImpl();
		UserInfo user = new UserInfo();
		UserKengen userkengen = new UserKengen();
		userkengen = loginServiceImpl.getUserKengen("xiongyl");
        assertEquals("20", userkengen.getKengenCd());
        userkengen = loginServiceImpl.getUserKengen("caowy");
        assertEquals("30", userkengen.getKengenCd());
        userkengen = loginServiceImpl.getUserKengen("liuyj");
        assertEquals("40", userkengen.getKengenCd());
	}
}
